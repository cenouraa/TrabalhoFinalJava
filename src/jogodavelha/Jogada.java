package jogodavelha;

public class Jogada {
    private int linha;
    private int coluna;

    public Jogada() {
        this.linha = linha;
        this.coluna = coluna;
    }

    public void setColuna(int coluna) {
        this.coluna = coluna;
    }

    public int getColuna() {
        return coluna;
    }

    public void setLinha(int linha) {
        this.linha = linha;
    }

    public int getLinha() {
        return linha;
    }
}
